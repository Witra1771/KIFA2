	<p><h2>Teknik Industri</h2></p><br>
	<p><b>Visi</b></p><br>

	<p>Menjadi  Program Studi Teknik Industri yang unggul dalam bidang Sistem Manufaktur yang  bertaraf internasional tahun 2031.</p><br>

	<p>Untuk lebih memberikan kejelasan makna dari rumusan visi tersebut maka berikut ini dipaparkan tentang pengertian dari beberapa kata kunci, yaitu :</p><br>

	<p>a. Unggul</p><br>

	<p>Program Studi Teknik Industri mampu meluluskan Sarjana yang memiliki pengetahuan yang      mumpuni di bidang Teknik Industri dan mampu bekerja secara profesional.</p><br>

	<p>b. Bertaraf Internasional</p><br>

	<p>Program Studi Teknik Industri yang diakui secara internasional dan  meluluskan Sarjana yang mampu bekerja di perusahaan bertaraf internasional.</p><br>

	<p><b>Misi</b></p><br>

	<p>Untuk mencapai visi tersebut di atas, maka Program Studi Teknik Industri melaksanakan misi  sebagai berikut:</p><br>
	<ul style="margin-left: 20px;">
		<li>Menyelenggarakan pendidikan dan pengajaran bidang Teknik Industri yang berkualitas dengan kurikulum berbasis kompetensi.</li><br>
		<li>Melakukan penelitian yang memiliki kontribusi bagi pengembangan ilmu pengetahuan khususnya bidang ilmu Teknik Industri.</li><br>
		<li>Melakukan pengabdian masyarakat yang sesuai dengan kebutuhan.</li><br>
		<li>Melakukan peningkatan kemampuan mahasiswa dalam berbahasa Inggris.</li><br>
		<li>Menjalin kerjasama dengan Perguruan Tinggi dalam maupun luar negeri.</li><br>
		<li>Menjalin kerjasama dengan lembaga pemerintahan dan swasta, baik di dalam maupun di luar negeri.</li><br>
	</ul><br>
	<p><b>Tujuan Program Studi Teknik Industri adalah sebagai berikut :</b></p><br>
	<ul style="margin-left: 20px;">
		<li>Memberdayakan dosen yang mampu mentransfer ilmu pengetahuan dan teknologi kepada mahasiswa, sehingga lulusannya memiliki daya saing Internasional.</li><br>
		<li>Menghasilkan lulusan yang mampu berbahasa Inggris, sehingga dapat diterima di perusahaan-perusahaan bertaraf Internasional.</li><br>
		<li>Menciptakan  lulusan yang memiliki kemampuan wirausaha secara global.</li><br>
		<li>Memberdayakan dosen dan menghasilkan mahasiswa yang memiliki wawasan keilmuan secara mutakhir.</li><br>
		<li>Menghasilkan karya penelitian dosen dan mahasiswa yang dapat dipublikasikan pada jurnal terakreditasi.</li><br>
		<li>Menghasilkan karya pengabdian yang bermanfaat bagi masyarakat secara luas. </li><br>
	</ul><br>
	<p><b>Sasaran :</b></p><br>

	<p style="">1. Periode I   pencapaian tahun 2016/2017 s/d 2020/2021</p><br>
    <p style="margin-left: 20px; width: 80%">Pencapaian pada periode ini adalah menjadi  Program Studi Teknik Industri yang unggul

        dalam bidang Sistem Manufaktur yang bertaraf regional Jawa Barat.</p><br>

    <p>2. Periode II  pencapaian tahun 2021/2022 s/d 2025/2026</p><br>
    <p style="margin-left: 20px; width: 80%">Pencapaian pada periode ini adalah menjadi  Program Studi Teknik Industri yang unggul

        dalam bidang Sistem Manufaktur yang bertaraf Nasional.</p><br>

    <p>3. Periode III pencapaian tahun 2026/2027 s/d 2030/2031</p><br>

    <p style="margin-left: 20px; width: 80%">Pencapaian pada periode ini adalah menjadi  Program Studi Teknik Industri yang unggul

        dalam bidang Sistem Manufaktur yang bertaraf Internasional.</p>
