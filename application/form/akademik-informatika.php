			<p><h2>Teknik Informatika</h2></p><br>
			<p><b>Visi</b></p><br>
			<center><p><b>"Pada tahun 2031 menjadi Program Studi Teknik Informatika yang Unggul di Bidang Rekayasa Teknologi Informasi berstandar Internasional"</b></p></center>

			<p><b>Misi</b></p><br>
			<ul style="padding-left: 20px;">
		<li>Melaksanakan pendidikan di bidang rekayasa teknologi informasi yang berkualitas dalam  menganalisa, merancang, membuat dan mengembangkan sistem informasi dan multimedia.</li><br>
		<li>Melaksanakan kegiatan penelitian dan pengembangan keilmuan yang mempunyai kontribusi terhadap kemajuan ilmu teknologi informasi tingkat nasional dan internasional.</p></li><br>
		<li>Melaksanakan pengabdian kepada masyarakat di bidang rekayasa teknologi informasi yang bersifat pada pemecahan masalah, pengembangan sistem, konsultasi, penyuluhan dan pelatihan yang profesional.</li><br>
		<li>Melaksanakan tata kelola, manajemen organisasi dan pelayanan terhadap civitas  akademik.</li><br>
		<li>Melaksanakan peningkatan sarana untuk mendukung proses akademik.</li><br>
		<li>Melaksanakan peningkatan sumber daya dosen, tenaga pendidik dan mahasiswa.</li><br>
		<li>Melaksankan kerjasama dengan instansi pemerintah, perguruan tinggi dan industri dalam kegiatan pendidikan, penelitian dan pengabdian kepada masyarakat.</li><br>
	</ul><br>
	<p><b>Tujuan dan Sasaran</b></p><br>
	<p><b>Tujuan :</b></p><br>
	<ul style="margin-left: 20px;">
		<li>Menghasilkan lulusan yang memiliki kemampuan di bidang rekayasa teknologi informasi tepat waktu dengan nilai akademik minimal 3,00.</li><br>
		<li>Menghasilkan lulusan yang memiliki kemampuan hardskills dan softskills dalam bidang Teknik Informatika dalam hal menganalisis, mendesain, membuat dan mengembangkan rekayasa teknologi informasi.</li><br>
		<li>Menghasilkan penelitian dibidang pengembangan rekayasa teknologi informasi yang dipublikasikan dijurnal nasional dan internasional.</li><br>
		<li>Menghasilkan kegiatan pengabdian kepada masyarakat yang melibatkan mahasiswa dan dosen dalam peran, fungsi, dan kontribusi teknik informatika di masyarakat.</li><br>
		<li>Menghasilkan kegiatan keterlibatan program studi dalam aktifitas pemberdayaan masyarakat dan peran serta terhadap kualitas kebijakan dan program pemerintah.</li><br><br>
		<li>Menghasilkan peningkatan tata kelola, manajemen dan layanan yang bersinergi.</li><br>
		<li>Menghasilkan kemampuan tenaga kependidikan, dosen dan mahaasiswa dalam proses belajar mengajar serta dalam penguasaan dan pemahaman terhadap bidang kajian yang ditekuni</li><br>
		<li>Menghasilkan kerjasama dengan instansi pemerintah, perguruan tinggi dan industri dalam kegiatan pendidikan, penelitian dan pengabdian kepada masyarakat yang menghasilkan inovasi untuk peningkatan kesejahteraan masyarakat.</li><br>
	</ul><br>

	<p><b>Sasaran :</b></p><br>

	<p style="">1. Prodi Teknik Informatika Unggul dalam Tridarma Perguruan Tinggi ditingkat Nasional dan Internasional.</p><br>
    <p>2. Menjadi Teknik Informatika bertaraf nasional dan internasional dengan penguatan dan penajaman tata kelola berbasis teknologi informasi.</p><br>
    <p>3. Menjadi Teknik Informatika bertaraf nasional dengan penanaman tata kelola berbasis teknologi informasi.</p><br>
