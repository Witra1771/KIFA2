			<p><b>Visi</b></p><br>
			<center><p><b>"Menjadi Program Studi Teknik Sipil yang Unggul dalam Rekayasa Struktur dan Berstandar Internasional pada Tahun 2031"</b></p></center>

			<br>
			<p>Unggul : mampu menjadi program studi yang menghasilkan lulusan yang dapat mengatasi permasalahan di bidang rekayasa struktur.</p><br>
			<p>Wirausaha : mampu menghasilkan lulusan yang siap berdikari dalam menjalankan usaha sendiri di bidang jasa konstruksi.</p><br>

			<p><b>Misi</b></p><br>
			<p>Misi Program Studi Teknik Sipil sebagai berikut : </p>
			<ul style="padding-left: 20px;"></ul><br>
		<li>Melaksanakan pendidikan Teknik Sipil yang berkualitas, berorientasi pada kebutuhan dunia jasa konstruksi dengan peningkatan kurikulum yang sesuai dengan kebutuhan dunia konstruksi global.</li><br>
		<li>Melakukan kegiatan penelitian yang berkontribusi kepada pemecahan masalah di bidang konstruksi dengan memperbanyak penelitian dan pengabdian dengan publikasi secara nasional dan internasional..</p></li><br>
		<li>Melaksanakan pengabdian kepada masyarakat dan dunia jasa konstruksi dengan melibatkan peran serta instansi pemerintah maupun swasta dalam penyuluhan dan pelatihan serta pengembangan metode pelaksanaan konstruksi.</li><br>
		<li>Meningkatkan kemampuan mahasiswa dalam berbahasa asing dengan pemberian kuliah dengan Bahasa inggris dan pelatihan toefl.</li><br>
		<li>Menjalin kerjasama dengan perusahaan, lembaga, instansi pemerintah maupun swasta upaya dalam peningkatan softkill dan hardskill dalam penyedian kerja praktek bagi mahasiswa Teknik sipil.</li><br>
		<li>Menyelenggarakan pembahasan permasalahan yang terjadi dalam dunia konstruksi beserta solusinya dalam perkuliahan dengan memperbanyak seminar dan pelatihan tentang dunia kontruksi.</li><br>
		<li>Meningkatkan kemampuan sumber daya manusia melalui pendidikan studi lanjut dengan pemberian beasiswa untuk staf dan dosen S1 prodi Teknik sipil.</li><br>
		<li>Melengkapi sarana infrastruktur dalam proses belajar mengajar dengan peningkatan gedung laboratorium dan sarana kelas yang ber ac.</li><br>
	</ul><br>

